
require("dotenv").config();

const axios = require("axios");

const DEFAULT_MODEL = "phi3:mini";
const DEFAULT_TIMEOUT_MS = 60000;
const MAX_HISTORY_ITEMS = 3;
const MAX_TEXT_LENGTH = 200;
const CACHE_MAX = 50;
const CACHE_TTL_MS = 5 * 60 * 1000;
const _replyCache = new Map();

const FALLBACK_REPLY_TEXT = "I'm having trouble right now. Please try again.";
const CONNECTION_REPLY_TEXT = "Ollama isn't running. Please start it and try again.";
const TIMEOUT_REPLY_TEXT = "That took too long. Try asking something shorter.";

function isDebugEnabled() {
  return String(process.env.DEBUG || "").trim().toLowerCase() === "true";
}

function debugAiLog(...args) {
  if (isDebugEnabled()) {
    console.log(...args);
  }
}

function getCacheKey(text, mode, language) {
  return `${normalizeMode(mode)}:${normalizeLanguage(language)}:${text.toLowerCase().trim().slice(0, 60)}`;
}

function getBaseUrl() {
  return String(process.env.OLLAMA_BASE_URL || "http://localhost:11434").trim();
}

function getRequestTimeoutMs() {
  const parsed = Number.parseInt(process.env.OLLAMA_TIMEOUT_MS, 10);
  return Number.isFinite(parsed) && parsed > 0 ? parsed : DEFAULT_TIMEOUT_MS;
}

const LANGUAGE_LABELS = {
  de: "German",
  en: "English",
  es: "Spanish",
  fr: "French",
  hi: "Hindi",
  ja: "Japanese",
};

const LANGUAGE_ALIASES = {
  de: "de", german: "de", "de-de": "de",
  en: "en", english: "en", "en-us": "en",
  es: "es", spanish: "es", "es-es": "es",
  fr: "fr", french: "fr", "fr-fr": "fr",
  hi: "hi", hindi: "hi", "hi-in": "hi",
  ja: "ja", japanese: "ja", "ja-jp": "ja",
};

function getModel() {
  return String(process.env.OLLAMA_MODEL || DEFAULT_MODEL).trim();
}

function normalizeMode(mode) {
  const normalized = String(mode || "assistant").trim().toLowerCase().replace(/\s+/g, "-");
  return ["assistant", "tutor", "translator", "customer-support"].includes(normalized) ? normalized : "assistant";
}

function normalizeLanguage(language) {
  const normalized = String(language || "en").trim().toLowerCase().replace(/\s+/g, "-");
  return LANGUAGE_ALIASES[normalized] || "en";
}

function formatLanguage(language) {
  return LANGUAGE_LABELS[normalizeLanguage(language)] || "English";
}

function normalizeHistory(history) {
  if (!Array.isArray(history)) return [];
  return history
    .filter(item => item && typeof item.text === "string")
    .slice(-MAX_HISTORY_ITEMS)
    .map(item => ({
      role: item.role === "assistant" ? "assistant" : "user",
      text: item.text.trim().slice(0, 240).replace(/^(User:|Assistant:|VOS:)\s*/gi, '').trim(),
    }))
    .filter(item => item.text);
}

function buildSystemPrompt({ language, mode, targetLanguage }) {
  const responseLanguage = formatLanguage(language);
  const targetLang = formatLanguage(targetLanguage || language);
  const resolvedMode = normalizeMode(mode);

  switch (resolvedMode) {
    case "assistant":
      return `You are VOS, a smart and friendly voice assistant. 
Reply in ${responseLanguage}. 
Keep answers under 3 sentences — short, clear, natural.
Never use bullet points, numbered lists, or markdown.
Speak like a helpful friend, not a robot.
Never start with 'Assistant:' or 'VOS:' — reply directly.
Reply directly. Never start with 'User:' or 'Assistant:'.
Never use bullet points or markdown.`;

    case "tutor":
      return `You are VOS, a patient and encouraging teacher.
Reply in ${responseLanguage}.
Explain in simple words a 10-year-old would understand.
Use one clear analogy or example.
Keep it under 4 sentences — short enough to speak aloud.
Never use bullet points or markdown formatting.
Reply directly. Never start with 'Assistant:' or 'VOS:'.
Never use bullet points or markdown.`;

    case "translator":
      return `You are VOS, a precise language translator.
Translate the user's text from ${formatLanguage(language)} to ${targetLang}.
Return ONLY the translated text — nothing else.
No explanations, no notes, no labels.
If you cannot translate, say: 'Translation not available.'
Reply directly. Never start with 'Assistant:' or 'VOS:'.
Never use bullet points or markdown.`;

    case "customer-support":
      return `You are VOS, a calm and helpful support agent.
Reply in ${responseLanguage}.
Acknowledge the issue briefly, then give one clear solution.
Keep it under 3 sentences. Sound human and empathetic.
Never use bullet points or markdown.
Reply directly. Never start with 'Assistant:' or 'VOS:'.
Never use bullet points or markdown.`;

    default:
      return `You are VOS, a helpful voice assistant. Reply in ${responseLanguage}. Be brief and natural. Never use bullet points or markdown. Reply directly.`;
  }
}

function buildPrompt({ history, language, mode, targetLanguage, text }) {
  const safeText = String(text || '').trim().slice(0, MAX_TEXT_LENGTH);
  const systemPrompt = buildSystemPrompt({ language, mode, targetLanguage });
  const recentHistory = normalizeHistory(history).slice(-3);

  let prompt = systemPrompt + '\n\n';

  if (recentHistory.length > 0) {
    prompt += 'Recent conversation:\n';
    for (const item of recentHistory) {
      const cleanText = item.text.replace(/^(User:|Assistant:|VOS:)\s*/gi, '').trim();
      if (item.role === 'user') {
        prompt += `Human: ${cleanText}\n`;
      } else {
        prompt += `VOS: ${cleanText}\n`;
      }
    }
    prompt += '\n';
  }

  prompt += `Human: ${safeText}\nVOS:`;

  return prompt;
}

function extractResponseText(response) {
  let text = String(response?.data?.response || '').trim();

  // Strip role prefixes
  text = text.replace(/^(VOS:|Assistant:|Human:|User:)\s*/gi, '').trim();

  // Strip markdown for voice
  text = text.replace(/\*\*(.*?)\*\*/g, '$1');
  text = text.replace(/\*(.*?)\*/g, '$1');
  text = text.replace(/#{1,6}\s/g, '');
  text = text.replace(/`{1,3}[^`]*`{1,3}/g, '');
  text = text.replace(/^\s*[\-\*\•]\s+/gm, '');
  text = text.replace(/^\s*\d+\.\s+/gm, '');
  text = text.replace(/\n{3,}/g, '\n\n');
  text = text.replace(/\[([^\]]+)\]\([^\)]+\)/g, '$1');

  // Fix incomplete sentences
  const sentences = text.split(/(?<=[.!?])\s+/);
  if (sentences.length > 1) {
    const last = sentences[sentences.length - 1];
    if (!last.match(/[.!?]$/)) sentences.pop();
    text = sentences.join(' ');
  }

  text = text.trim();
  return text || FALLBACK_REPLY_TEXT;
}

async function generateReply({ text, history, mode, language, targetLanguage }) {
  const cacheKey = getCacheKey(text, mode, language);
  const cached = _replyCache.get(cacheKey);
  if (cached && Date.now() - cached.ts < CACHE_TTL_MS) {
    debugAiLog('[AI] Cache hit');
    return cached.text;
  }

  const prompt = buildPrompt({ history, language, mode, targetLanguage, text });
  const model = getModel();
  const baseUrl = getBaseUrl();
  const url = `${baseUrl.replace(/\/$/, "")}/api/generate`;

  try {
    debugAiLog('PROMPT:', prompt.slice(0, 200) + '...');
    const res = await axios.post(url, {
      model,
      prompt,
      stream: false,
      options: {
        num_predict: parseInt(process.env.OLLAMA_MAX_TOKENS || '80'),
        num_ctx: 1024,
        temperature: 0.7,
        top_k: 40,
        top_p: 0.9,
        repeat_penalty: 1.15,
        stop: ['Human:', 'User:', '\n\nHuman:', '\n\nUser:', 'VOS:', 'Assistant:'],
      },
    }, { timeout: getRequestTimeoutMs() });

    const replyText = extractResponseText(res);

    // Cache
    if (_replyCache.size >= CACHE_MAX) {
      _replyCache.delete(_replyCache.keys().next().value);
    }
    _replyCache.set(cacheKey, { text: replyText, ts: Date.now() });

    return replyText;
  } catch (error) {
    const code = error.code;
    const status = error.response?.status;

    if (status === 404) return `Model not found. Run: ollama pull ${model}`;
    if (code === 'ECONNREFUSED' || code === 'EHOSTUNREACH') return CONNECTION_REPLY_TEXT;
    if (code === 'ETIMEDOUT' || code === 'ECONNABORTED') return TIMEOUT_REPLY_TEXT;
    return FALLBACK_REPLY_TEXT;
  }
}

function isAiFailureReply(text) {
  const normalized = String(text || '').trim();
  return [FALLBACK_REPLY_TEXT, CONNECTION_REPLY_TEXT, TIMEOUT_REPLY_TEXT].includes(normalized) ||
    normalized.startsWith('Model not found');
}

module.exports = {
  generateReply,
  isAiFailureReply,
  getRequestTimeoutMs,
};

